package Dot;

public class accumulated_temperature_List {
    String date;
    int rain;
    int count;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getTemp() {
        return rain;
    }

    public void setTemp(int temp) {
        this.rain = temp;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
